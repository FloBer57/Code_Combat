# Un Booléen est une valeur qui est True ou False
# Le  symbole ==  signifie "est égal à?"
# Ainsi, écrire A == B pose la question : "A est égal à B ?"
# La réponse est une valeur booléenne!
# Cliquez sur le bouton "Hints" si ceci prête à confusion!

# Question: 2 == 3 
# Dites la réponse correcte:
hero.say(False)

# Question: 3 == 3
# Répondre True ou False à la question 2:
